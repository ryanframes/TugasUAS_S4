    private void cmdDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdDeleteActionPerformed
        // TODO add your handling code here:
        if (txtKodeBuku.getText().equals("")) {
            cls.showMsg("Harap pilih buku yang akan dihapus !", "Hapus Gagal", 2);
            txtKodeBuku.grabFocus();
            return;
        }
        int btn = JOptionPane.YES_NO_OPTION;
        int res=JOptionPane.showConfirmDialog(null,"Apakah anda yakin ingin "
            + "menghapus data buku [" + txtKodeBuku.getText() + " - " + 
            txtNamaBuku.getText()  + "] ?","Hapus Data",btn);
        if (res==JOptionPane.YES_OPTION){
            try {
                oConn.setData("delete from t_buku where kd_buku = '" 
                        + txtKodeBuku.getText() + "'");
                cmdDisplay.doClick();
                cls.showMsg("Kode Buku ["+ txtKodeBuku.getText() +" - " + 
                        txtNamaBuku.getText() + "] berhasil di hapus.", "Hapus Berhasil", 0);
                textMode(true);
                clrForm();
            } catch (SQLException ex) {
                cls.showMsg(ex.getMessage(), "Error", 0);
            }
        }
    }//GEN-LAST:event_cmdDeleteActionPerformed

