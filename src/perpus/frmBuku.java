    private boolean allowSave() {
        if (txtKodeBuku.getText().equals("")) {
            cls.showMsg("Kode Buku harus di isi", "Simpan Gagal", 0);
            txtKodeBuku.grabFocus();
            return false;
        }
        if (txtKodeBuku.isEnabled()) { //validasi kode buku
            try {
                ResultSet rs = oConn.getData("select nm_buku from t_buku where " + 
                    "kd_buku='"+ txtKodeBuku.getText().toString() +"'");
                if (rs.next()) {
                    cls.showMsg("Kode Buku [" + txtKodeBuku.getText().toString() + "] sudah "
                            + "terdaftar dengan nama [" + rs.getString("nm_buku") + "]", "Simpan Gagal", 0);
                }
                rs.close();
                txtKodeBuku.grabFocus();
                return false;
            } catch (SQLException ex) {
                Logger.getLogger(frmBuku.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (txtNamaBuku.getText().equals("")) {
            cls.showMsg("Nama Buku harus di isi", "Simpan Gagal", 0);
            txtNamaBuku.grabFocus();
            return false;
        }
        if (txtThn.getText().equals("0")) {
            cls.showMsg("Tahun Penerbit harus di isi", "Simpan Gagal", 0);
            txtThn.grabFocus();
            return false;
        }
        if (txtNamaPenerbit.getText().equals("")) {
            cls.showMsg("Nama Penerbit harus di isi", "Simpan Gagal", 0);
            txtNamaPenerbit.grabFocus();
            return false;
        }
        if (txtNamaPenulis.getText().equals("")) {
            cls.showMsg("Nama Penulis harus di isi", "Simpan Gagal", 0);
            txtNamaPenulis.grabFocus();
            return false;
        }
        if (txtDeskripsi.getText().equals("")) {
            cls.showMsg("Deskripsi Buku harus di isi", "Simpan Gagal", 0);
            txtDeskripsi.grabFocus();
            return false;
        }
        if(txtStok.getText().equals("")) txtStok.setText("0"); 
        if (txtStok.getText().equals("0")) {
            cls.showMsg("Jumlah Stok harus lebih besar dari nol(0)", "Simpan Gagal", 0);
            txtStok.grabFocus();
            return false;
        }
        return true;
    }    
