    private void cmdSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdSaveActionPerformed
        // TODO add your handling code here:
        if (!allowSave()){ return; }
        try {
            if (txtKodeBuku.isEnabled()) {
                oConn.setData("insert into t_buku (kd_buku, nm_buku, "
                    + "th_penerbit, nm_penerbit, nm_penulis, deskripsi, stok_buku) "
                    + "values ('" + txtKodeBuku.getText() + "','" + txtNamaBuku.getText() + "', "
                    + "'" + txtThn.getText() + "', '" + txtNamaPenerbit.getText() + "', "
                    + "'" +  txtNamaPenulis.getText() + "', '" + txtDeskripsi.getText() + "', "
                    + "'" + txtStok.getText() + "')");
            }
            else {
                oConn.setData("update t_buku set nm_buku = '" + txtNamaBuku.getText() + "', "
                    + "th_penerbit = " + txtThn.getText() + ", nm_penerbit = '" + txtNamaPenerbit.getText() + "', "
                    + "nm_penulis = '" + txtNamaPenulis.getText() + "', deskripsi = '" + txtDeskripsi.getText() + "', "
                    + "stok_buku = '" + txtStok.getText() + "' where kd_buku = '" + txtKodeBuku.getText() + "'");
            }
            textMode(false);
            clrForm();
            cls.showMsg("Simpan data berhasil !", "Simpan", 1);
        } catch (SQLException ex) {
            cls.showMsg(ex.getSQLState(), "Gagal Simpan", 0);
        }
        cmdDisplay.doClick();
    }//GEN-LAST:event_cmdSaveActionPerformed

