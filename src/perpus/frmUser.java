    private void cmdDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdDeleteActionPerformed
        // TODO add your handling code here:
        if (txtUser.getText().equals("")) {
            cls.showMsg("Harap pilih user yang akan dihapus !", "Hapus Gagal", 2);
            txtUser.grabFocus();
            return;
        }
        int btn = JOptionPane.YES_NO_OPTION;
        int res=JOptionPane.showConfirmDialog(null,"Apakah anda yakin ingin "
            + "menghapus data terpilih ?","Hapus Data",btn);
        if (res==JOptionPane.YES_OPTION){
            try {
                oConn.setData("delete from t_user where nm_user = '"
                    + txtUser.getText() + "'");
                cmdDisplay.doClick();
                cls.showMsg("User ["+ txtUser.getText() +"] berhasil di hapus.", 
                        "Hapus Berhasil", 0);
                textMode(true);
                clrForm();
            } catch (SQLException ex) {
                cls.showMsg(ex.getMessage(), "Error", 0);
            }
        }
    }//GEN-LAST:event_cmdDeleteActionPerformed

