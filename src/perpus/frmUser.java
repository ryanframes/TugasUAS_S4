    private void cmdSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdSaveActionPerformed
        // TODO add your handling code here:
        if (!allowSave()){ return; }
        int lvl;
        if (optAdmin.isSelected()) {
            lvl=1;
        }
        else
        {
            lvl=2;
        }
        try {
            if (txtUser.isEnabled()) {
                oConn.setData("insert into t_user (nm_user, password, "
                    + "level) values ('" + txtUser.getText() + "','" + String.valueOf(txtPassword.getPassword()) + "', "
                    + "" + lvl + ")");
            }
            else {
                oConn.setData("update t_user set password = '" + String.valueOf(txtPassword.getPassword()) + "', "
                    + "level = " + lvl + " where nm_user = '" + txtUser.getText() + "'");
            }
            textMode(false);
            clrForm();
            cls.showMsg("Simpan data berhasil !", "Simpan", 1);
        } catch (SQLException ex) {
            cls.showMsg(ex.getSQLState(), "Gagal Simpan", 0);
        }
        cmdDisplay.doClick();
    }//GEN-LAST:event_cmdSaveActionPerformed

