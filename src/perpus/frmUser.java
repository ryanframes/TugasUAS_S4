    private void tblUsersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsersMouseClicked
        // TODO add your handling code here:
        int row=tblUsers.getSelectedRow();
        txtUser.setText((String)tblUsers.getValueAt(row, 0));
        txtPassword.setText((String)tblUsers.getValueAt(row, 1));
        String lv=(String)tblUsers.getValueAt(row, 2);
        if (lv.equals("ADMIN")) {
            optAdmin.setSelected(true);
            optStaff.setSelected(false);
        }
        else {
            optAdmin.setSelected(false);
            optStaff.setSelected(true);
        }
            
        textMode(false);
    }//GEN-LAST:event_tblUsersMouseClicked

